package com.groupT01.model;

public class travelPlan {
	public int pid;// 待选方案编号
	public int gid;// 导游编号
	public int sid;// 旅游方案编号

	public int getPid() {
		return pid;
	}

	public int getGid() {
		return gid;
	}

	public int getSid() {
		return sid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public void setGid(int gid) {
		this.gid = gid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

}
